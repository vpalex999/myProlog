/*
1. Напишитепредикат p(+X, +N, -L) - истинныйтогда итолько тогда,
когда L - список из N раз повторенных элементов X.

Тест:
p(a,0, []).
L = [].

p(a,1,[]).
L = [a].

p(a,2,[]).
L = [a,a].
*/

% Задача 1

p(X,N,L):-
    N>0,
    N1 is N -1,
    p(X,N1,L).

p(X,0,L):-
    add(X,L,L).

add(H,T,[H|T]).


/*
