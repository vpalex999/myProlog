tests:
p2([],L).
L=[].

p2([a],L).
L=[[a]]

p2([a,b],L).
L=[[a],[b]].

*/

% (2)

p2([],[]).

p2(L,L1):-
    p2(L,[],L1).

p2([],L,L):-
    inverse(L,L1).

p2([H|T], L2, L1):-
    p2(T, [[H]|L2], L1).


inverse(L,L1):-
    inverse(L,[],L1).

inverse([],L,L).
inverse([H|T],L2,L1):-
